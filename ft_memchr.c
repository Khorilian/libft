#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if ((const char*)s[i] == (unsigned char)c)
			return (&s[i]);
		i++;
	}
	return (0);
}
